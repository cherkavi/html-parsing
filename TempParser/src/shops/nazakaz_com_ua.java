	class UserSection extends NextSection {
		public UserSection(String name, String url) {
			super(name, url);
		}

		private int pageCounter = 0;

		@Override
		public String getUrlToNextPage() {
			pageCounter++;
			if (pageCounter > 1) {
				String value=this.getUrl().trim();
				if(!value.endsWith("/")){
					value=value+"/";
				}
				return value+"?page="+pageCounter;
			}
			return this.getUrl();
		}
	}

