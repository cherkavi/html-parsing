	@Override
	public String getShopUrlStartPage() {
		return "http://www.acdc.kiev.ua";
	}

	@Override
	protected String getCharset() {
		return "windows-1251";
	}

	@Override
	protected ISectionFinder getSectionFinder() throws Exception {
		// import org.w3c.dom.Node;
		// import
		// shop_list.html.parser.engine.multi_page.section_finder.NodeListFinderByUrl;
		// ArrayList<Node> listOfNode=(new NodeListFinderByUrl(parser,
		// getCharset(),
		// "/html/body/div[4]/div/div[2]/div[3]/table/tbody/tr[*]/td[@width="25%"]/div/div/ul/li[*]/a")).getNodeListByUrl(this.getShopUrlStartPage());
		ArrayList<Node> listOfNode = this.parser
				.getNodeListFromUrl("http://www.acdc.kiev.ua", "windows-1251",
						"/HTML/BODY/TABLE[3]/TBODY/TR/TD[1]/UL[@id=\"l_menu\"]/LI[*]/A");
		return new DirectFinder(listOfNode,
				new IResourceFromElementExtractor() {
					@Override
					public INextSection getResourceSection(Element element) {
						String url = addHttpPreambula(
								"http://www.acdc.kiev.ua", element
										.getAttribute("href"));
						return new UserSection(element.getTextContent(), url);
					}
				});
	}

	class UserSection extends NextSection {
		public UserSection(String name, String url) {
			super(name, url);
		}

		private int pageCounter = 0;
		private String preambula=null;
		private String postambula="/index.html";
		@Override
		public String getUrlToNextPage() {
			pageCounter++;
			if (pageCounter > 1) {
				// http://www.acdc.kiev.ua/ru/products/procategory/149/index.html
				// http://www.acdc.kiev.ua/ru/products/procategory/149/pno/2/index.html
				// http://www.acdc.kiev.ua/ru/products/procategory/149/pno/3/index.html
				if(preambula==null){
					
					int index=this.getUrl().indexOf(postambula);
					if(index>0){
						preambula=this.getUrl().substring(0, index)+"/pno/";
					}
				}
				return preambula+pageCounter+postambula;
			}
			return this.getUrl();
		}
	}

	private ESectionEnd[] conditions = new ESectionEnd[] {
			ESectionEnd.NEXT_RECORDS_REPEAT_LAST,
			ESectionEnd.NEXT_RECORDS_LOAD_ERROR };

