	class UserSection extends NextSection {
		public UserSection(String name, String url) {
			super(name, url);
		}

		private int pageCounter = 0;
		private String preambula=null;
		@Override
		public String getUrlToNextPage() {
			pageCounter++;
			if (pageCounter > 1) {
				// http://www.toxic.com.ua/catalog/216/205/
				// http://www.toxic.com.ua/catalog/216/page1/prod/
				// http://www.toxic.com.ua/catalog/216/page2/prod/
				if(this.preambula==null){
					String value=this.getUrl().trim();
					if(value.endsWith("/")){
						value=value.substring(0, value.length()-1);
					}
					int index=value.lastIndexOf('/');
					this.preambula=value.substring(0,index)+"/page";
				}
				return this.preambula+(pageCounter-1)+"/prod/";
			}
			return this.getUrl();
		}
	}

