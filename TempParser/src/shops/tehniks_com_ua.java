		ArrayList<Node> listOfNode = this.parser
				.getNodeListFromUrl(
						"http://www.tehniks.com.ua",
						"utf-8",
						"/HTML/BODY/TABLE/TBODY/TR/TD/TABLE[3]/TBODY/TR[1]/TD[2]/TABLE[@class=\"100x100pro\"]/TBODY/TR/TD/TABLE[2]/TBODY/TR[3]/TD/TABLE/TBODY/TR[*]/TD[*]/A[@class=\"standard\"]");
